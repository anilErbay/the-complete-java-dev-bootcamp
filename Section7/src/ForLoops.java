public class ForLoops {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // ***
        System.out.println();
        // ***

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello, world!");
        }

        // ***
        System.out.println();
        // ***

        for (int i = 1; i < 11; i++) {
            System.out.println(i * 5);
        }

        // ***
        System.out.println();
        // ***

        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }

        // ***
        System.out.println();
        // ***

        for (int i = 0; i <= 4; i++) {
            System.out.print(i + " ");
        }

        // ***
        System.out.println();
        // ***

        System.out.print("Incoming counters: ");
        for (int i = 0; i <= 4; i++) {
            System.out.print(i + " ");
        }
    }
}
